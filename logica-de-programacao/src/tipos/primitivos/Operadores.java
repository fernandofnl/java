package tipos.primitivos;

public class Operadores {

    public static void main(String[] args) {

        var bb = 122;
        double a = 10, b = 15.1;
        int soma;

        soma = (int) a + (int) b;
        bb = 2;
        var oi = bb + a;

        System.out.println(a);
        System.out.println(soma);
        System.out.println(oi);

    }
}
